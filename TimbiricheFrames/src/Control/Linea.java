package Control;

/**
 *
 * @author Angel
 */
public class Linea {
    private double x;
    private double y;
    private double w;
    private double h;

    public Linea(double x, double y, double w, double h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    @Override
    public String toString() {
        return "Linea{" + "x=" + x + ", y=" + y + ", w=" + w + ", h=" + h + '}';
    }
    
    

    
    
    
}
